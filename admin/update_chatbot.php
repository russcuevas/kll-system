if (isset($_GET['id'])) {
    $id = $_GET['id'];

    $query = "SELECT * FROM tbl_chatbot WHERE id = :id";
    $stmt = $conn->prepare($query);
    $stmt->execute(['id' => $id]);
    $chatbot = $stmt->fetch();

    if (!$chatbot) {
        $_SESSION['errors'] = "Chatbot not found.";
        header('Location: chatbot.php');
        exit;
    }

    // Check if the form has been submitted
    if ($_SERVER['REQUEST_METHOD'] == 'POST') {
        $sequence = $_POST['sequence'];
        $chat_question = $_POST['chat_question'];
        $bot_response = $_POST['bot_response'];

        try {
            $updateQuery = "UPDATE tbl_chatbot SET sequence = :sequence, chat_question = :chat_question, bot_response = :bot_response WHERE id = :id";
            $stmt = $conn->prepare($updateQuery);
            $stmt->execute([
                'sequence' => $sequence,
                'chat_question' => $chat_question,
                'bot_response' => $bot_response,
                'id' => $id
            ]);

            $_SESSION['success'] = "Chatbot updated successfully!";
            header("Location: update_chatbot.php?id=$id");
            exit();
        } catch (Exception $e) {
            $_SESSION['errors'] = "Failed to update chatbot. Error: " . $e->getMessage();
        }
    }
} else {
    $_SESSION['errors'] = "No ID provided.";
    header('Location: chatbot.php');
    exit;
